package mycompany.exercises.Matriz;

// Clase para representar una posición en una matriz
public class Posicion {
    public int fila; // Número de fila
    public int columna; // Número de columna

    // Constructor que inicializa la fila y la columna de la posición
    public Posicion(int fila, int columna) {
        this.fila = fila; // Asignar el valor de la fila proporcionado
        this.columna = columna; // Asignar el valor de la columna proporcionado
    }
}

